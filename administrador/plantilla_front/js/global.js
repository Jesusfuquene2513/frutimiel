$(document).ready(function(){
	$(".owl-prev , .owl-next").css("width","60px");
	$(".owl-prev , .owl-next").css("height","60px");
	$(".owl-prev , .owl-next").css("height","60px");
$(".owl-prev , .owl-next").css("background","#167c47")


$(".owl-prev , .owl-next").mouseover(function(){
	$(".owl-prev , .owl-nex").css("color","white");
$(this).css("color","#fcc41a");
$(this).css("font-weight","bolder");
});

$(".owl-prev , .owl-next").mouseout(function(){
	$(".owl-prev , .owl-nex").css("color","white");
$(this).css("color","white");

});




$(".btn-abrir-menu").mouseover(function(){

	$(this).css("background-color","rgb(120, 68, 44");

});

$(".btn-abrir-menu").mouseout(function(){

	$(this).css("background-color","#167c47");

});









});

